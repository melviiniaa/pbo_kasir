
package ProjekAkhir;


public class Paket {
    private String kode_paket;
    private String masa_paket;
    private String harga_paket;

    public String getKode_paket() {
        return kode_paket;
    }

    public void setKode_paket(String kode_paket) {
        this.kode_paket = kode_paket;
    }

    public String getMasa_paket() {
        return masa_paket;
    }

    public void setMasa_paket(String masa_paket) {
        this.masa_paket = masa_paket;
    }

    public String getHarga_paket() {
        return harga_paket;
    }

    public void setHarga_paket(String harga_paket) {
        this.harga_paket = harga_paket;
    }
}
