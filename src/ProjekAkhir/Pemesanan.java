
package ProjekAkhir;


public class Pemesanan {
    private int no_pemesanan;
    private int nik;
    private String kode_paket;
    private String alamat;

    public int getNo_pemesanan() {
        return no_pemesanan;
    }

    public void setNo_pemesanan(int no_pemesanan) {
        this.no_pemesanan = no_pemesanan;
    }

    public int getNik() {
        return nik;
    }

    public void setNik(int nik) {
        this.nik = nik;
    }

    public String getKode_paket() {
        return kode_paket;
    }

    public void setKode_paket(String kode_paket) {
        this.kode_paket = kode_paket;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    
}
